package es.aragon.ironsheep.data.entities;

import java.util.Date;

public class GreenZone {

	private float grass;
	private float erro;
	private Date date;

	public float getGrass() {
		return grass;
	}

	public void setGrass(float grass) {
		this.grass = grass;
	}

	public float getErro() {
		return erro;
	}

	public void setErro(float erro) {
		this.erro = erro;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}
